    private void selectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectActionPerformed
        // TODO add your handling code here:
//        System.out.println(merekPakaian.getText());
//        System.out.println(namaPakaian1.getText());
//        System.out.println(hargaPakaian1.getText());
        if (merekPakaian != null
                && namaPakaian1 != null
                && hargaPakaian1 != null){
            Pakaian pakaian = new Pakaian(
                    namaPakaian1.getText(), merekPakaian.getText(), Double.parseDouble(hargaPakaian1.getText())
            );
            service.setSelected(pakaian);
            this.populateTable2();

            AtomicReference<Double> totalSelected = new AtomicReference<>(0.0);
            service.selected().forEach(value -> {
                totalSelected.updateAndGet(
                        currentTotal -> currentTotal + value.getHarga()
                );
            });
            total.setText(String.valueOf(totalSelected));
        }

    }//GEN-LAST:event_selectActionPerformed

