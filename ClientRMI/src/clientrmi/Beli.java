    private void populateTable(){

        List<Pakaian> list = service.getGetAllPakaian();

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        model.setRowCount(0);

        int no = 1;
        for (Pakaian pakaian : list) {
            model.addRow(new Object[]{no++, pakaian.getNama(), pakaian.getMerek(), pakaian.getHarga()});
        }
    }

    private void populateTable2(){

        List<Pakaian> list = service.selected();

        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();

        model.setRowCount(0);

        int no = 1;
        for (Pakaian pakaian : list) {
            model.addRow(new Object[]{no++, pakaian.getNama(), pakaian.getMerek(), pakaian.getHarga()});
        }
    }

