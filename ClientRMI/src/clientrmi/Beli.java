    private void beliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_beliActionPerformed
        // TODO add your handling code here:
        double hargaTotal = Double.parseDouble(total.getText());
        if (hargaTotal >= 150_000){
           double potongan = service.hitung(hargaTotal);
           discount.setText(String.valueOf(potongan));
           double hargaAkhir = hargaTotal - potongan;
           total.setText(String.valueOf(hargaAkhir));
        }else {
           total.setText(String.valueOf(hargaTotal));

        }
    }//GEN-LAST:event_beliActionPerformed

